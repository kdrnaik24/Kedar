package POJO;

public class Book {
	int BookId;
	String BookName;
	String AutorName;
	public int getBookId() {
		return BookId;
	}
	public void setBookId(int bookId) {
		BookId = bookId;
	}
	public String getBookName() {
		return BookName;
	}
	public void setBookName(String bookName) {
		BookName = bookName;
	}
	public String getAutorName() {
		return AutorName;
	}
	public void setAutorName(String autorName) {
		AutorName = autorName;
	}

	
	
}
